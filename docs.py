# Скрипт для автоматической генерации документации проекта
# Запускается автоматически через CI/CD при каждом обновлении кода

def generate_docs():
    # Создаём файл документации
    docs = """
# Документация проекта — Игра "Угадай число"

## Описание проекта
Игра в которой компьютер загадывает число от 1 до 100
а игрок должен его угадать получая подсказки.

## Файлы проекта
- game.py — основной код игры
- test_game.py — unit-тесты
- main.yml — конфигурация CI/CD

## Функции game.py

### generate_number()
Генерирует случайное число от 1 до 100.
Возвращает: случайное целое число

### check_guess(secret, guess)
Проверяет догадку игрока.
Параметры: secret — загаданное число, guess — догадка
Возвращает: "больше", "меньше" или "угадал"

### is_valid_number(number)
Проверяет что число в диапазоне 1-100.
Возвращает: True или False

### play_game()
Основная функция запуска игры.

## Тесты test_game.py
- test_check_guess_больше — проверка подсказки больше
- test_check_guess_меньше — проверка подсказки меньше  
- test_check_guess_угадал — проверка определения победы
- test_generate_number — проверка диапазона числа
- test_is_valid_number — проверка валидации числа

## CI/CD Pipeline
Автоматически запускается при каждом коммите.
Выполняет: проверку файлов и запуск всех тестов.
    """
    
    # Сохраняем документацию в файл
    with open("documentation.md", "w", encoding="utf-8") as f:
        f.write(docs)
    print("Документация успешно сгенерирована!")

if __name__ == "__main__":
    generate_docs()
